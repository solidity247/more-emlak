"use client"

import { useState } from "react"
import { useLocale } from "@/lib/locale-context"
import { Button } from "@/components/ui/button"
import { ChevronDown, ChevronUp } from "lucide-react"

const fullText = `Мерсин, расположенный на южном побережье Турции, является прекрасным местом для инвестиций в недвижимость. В этом регионе есть много возможностей для покупки различных объектов недвижимости, таких как виллы, студии, коммерческая недвижимость и участки земли.

Одним из привлекательных районов Мерсина является Эрдемли. Этот район славится своими живописными пляжами и прекрасным видом на море. Здесь вы можете купить виллу с видом на море и наслаждаться прекрасной погодой и красивыми закатами каждый день.

Еще одним популярным районом для строительства недвижимости в Мерсине является Томюк. Здесь представлены различные варианты недвижимости, начиная от студий до больших вилл. Благодаря удобному расположению и доступным ценам, покупка недвижимости в Томюке может быть очень выгодной инвестицией.

Если вы ищете недвижимость в Мерсине с комфортными условиями проживания, то район Мезитли может быть идеальным выбором для вас. Здесь представлены различные типы недвижимости, от студий до вилл, и вы можете выбрать то, что лучше всего соответствует вашим потребностям.

Команда проекта недвижимости в Мерсине предлагает различные варианты недвижимости в каталоге. Вы можете выбрать участок земли для строительства своего дома или купить готовую недвижимость. Кроме того, доступна система рассрочки, что делает покупку недвижимости еще более привлекательной.

Мерсин также известен своим удивительным расположением. Он находится недалеко от Стамбула и предлагает прекрасные виды на море и горы. Кроме того, вы можете легко добраться до других популярных туристических мест, таких как Анталия, Алания, Трабзон и Измир.

Территория Мерсина представлена различными объектами инфраструктуры, такими как детские площадки, рестораны, бары и отели. Вы можете наслаждаться прекрасной турецкой кухней и отдыхать на пляже, находясь всего в нескольких шагах от вашего дома.

Если вы мечтаете о проживании в спокойной деревне или на даче, Мерсин предлагает такие возможности. Вы можете купить недвижимость в окружении лесов или с видом на пролив. Это идеальное место для тех, кто ищет уединение и наслаждение природой.

Мерсин - это не только красивый город с прекрасными пляжами и природой, но и активно развивающийся регион с множеством возможностей для инвестиций в недвижимость. Независимо от того, ищете ли вы недвижимость для проживания или для коммерческой застройки, Мерсин предлагает много интересных вариантов. Не упустите возможность стать владельцем недвижимости в этом прекрасном месте!`

export function MersinTextSection() {
  const { t } = useLocale()
  const [expanded, setExpanded] = useState(false)

  const paragraphs = fullText.split("\n\n")
  const displayParagraphs = expanded ? paragraphs : paragraphs.slice(0, 2)

  return (
    <section className="bg-card py-16 md:py-24">
      <div className="mx-auto max-w-4xl px-4">
        <h2 className="mb-8 text-center text-3xl font-bold text-foreground md:text-4xl">
          {t("mersin.title")}
        </h2>

        <div className="flex flex-col gap-4">
          {displayParagraphs.map((p, i) => (
            <p key={i} className="leading-relaxed text-muted-foreground">
              {p}
            </p>
          ))}
        </div>

        {paragraphs.length > 2 && (
          <div className="mt-6 text-center">
            <Button
              variant="ghost"
              onClick={() => setExpanded(!expanded)}
              className="text-primary hover:text-primary/80"
            >
              {expanded ? (
                <>
                  <ChevronUp className="mr-1 h-4 w-4" />
                  {"Свернуть"}
                </>
              ) : (
                <>
                  <ChevronDown className="mr-1 h-4 w-4" />
                  {"Читать далее"}
                </>
              )}
            </Button>
          </div>
        )}
      </div>
    </section>
  )
}
